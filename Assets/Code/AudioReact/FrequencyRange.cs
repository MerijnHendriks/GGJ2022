namespace AudioReact
{
    public enum FrequencyRange
    {
        SubBase,        // 20 - 60 Hz
        Bass,           // 60 - 250 Hz
        LowMidrange,    // 250 - 500 Hz
        Midrange,       // 500 - 2000 Hz
        UpperMidrange,  // 2000 - 4000 Hz
        High,           // 4000 - 6000 Hz
        VeryHigh,       // 6000 - 20000 Hz
        Decibel,        // 20 - 20000 Hz
    }
}
